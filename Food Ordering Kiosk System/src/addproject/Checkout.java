    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int result = JOptionPane.showConfirmDialog(null,"Sure? You want to make payment for Dine-in?", "Swing Tester",
               JOptionPane.YES_NO_OPTION,
               JOptionPane.QUESTION_MESSAGE);
        switch (result) {
            case JOptionPane.YES_OPTION:
                JOptionPane.showMessageDialog(null, "Your Order has been placed Successfully");
                try{
            
            Connection conn = DriverManager.getConnection("jdbc:derby://localhost:1527/dcoms","dcoms","dcoms");
            Statement st = conn.createStatement();
            String sql = "DELETE FROM PL_ORDER2";
            st.executeUpdate(sql);
            Customer_Login info = new Customer_Login();
            info.setVisible(true);
 
        }
        catch (SQLException ex) {
            Logger.getLogger(Customer_Login.class.getName()).log(Level.SEVERE, null, ex);
        }
                break;
            case JOptionPane.NO_OPTION:
                break;
            default:
                JOptionPane.showMessageDialog(null, "None selected");
                break;
        }
    }//GEN-LAST:event_jButton3ActionPerformed

